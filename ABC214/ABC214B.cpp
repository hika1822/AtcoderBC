#include <bits/stdc++.h>
using namespace std;

int main()
{
/***********************入力************************/
    int S, T;
    cin >> S >> T;
/////////////////////////////////////////////////////
/***********************演算************************/
    int result = 0;

    for(int a = 0; a <= S; a++)
    {
        for(int b = 0; b <= S; b++)
        {
            for(int c = 0; c <= S; c++)
            {
                if((a + b + c <= S) && (a * b * c <= T))
                {
                    result++;
                }
            }
        }
    }
/////////////////////////////////////////////////////
/***********************出力************************/
    cout << result << endl;
    return 0;
}