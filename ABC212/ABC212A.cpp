#include <bits/stdc++.h>
using namespace std;

int main()
{
/////////////////////////////////////////////////////
/***********************入力************************/
    int A, B;
    cin >> A >> B;

/////////////////////////////////////////////////////
/***********************演算************************/

    if(0 < A && B == 0)
    {
        cout << "Gold" << endl;
    }
    if(0 < B && A == 0)
    {
        cout << "Silver" << endl;
    }
    if(0 < B && 0 < A)
    {
        cout << "Alloy" << endl;
    }

/////////////////////////////////////////////////////
/***********************出力************************/

}